def is_prime(n):
    """Checks if a number is prime."""
    if n <= 1:
        return False
    for i in range(2, int(n ** 0.5) + 1):
        if n % i == 0:
            return False
    return True

print(is_prime(7))

for i in range(2,100000):
    if is_prime(i) and is_prime(i+3):
        print(i)


